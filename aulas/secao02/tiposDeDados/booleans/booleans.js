// Bolleans são tipos de dados que representam verdadeiro ou falso
// São muito utilizados em estruturas condicionais

// Exemplo de uso de booleans
console.log(5 > 3); // true
console.log(5 < 3); // false

console.log(typeof(5 > 3));
console.log(typeof(5 < 3));

// Outro exemplo
console.log(true);
console.log(false);

console.log(typeof(true));
console.log(typeof(false));

// Boolean em condicionais
let falso = false;

if (falso == true) {
    console.log("É trueeeee")
} else {
    console.log("É falseeee")
}

