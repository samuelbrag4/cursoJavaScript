// Praticando as operações no console

// Adição
console.log(typeof(2 + 3));

// Subtração
console.log(typeof(2 - 3));

// Multiplicação
console.log(typeof(2 * 3));

// Divisão
console.log(typeof(2 / 3));

// Módulo
console.log(typeof(2 % 3));

// Exponenciação
console.log(typeof(2 ** 3));

// Ordem de precedência
console.log(typeof(2 + 3 * 5));

// Alterando a ordem de precedência
console.log(typeof((2 + 3) * 5));